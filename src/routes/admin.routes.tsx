import { ReactNode } from "react";
import AdminDashboard from "../components/layout/admin/AdminDashboard";
import CreateAdmin from "../components/layout/admin/CreateAdmin";
import CreateFaculty from "../components/layout/admin/CreateFaculty";
import CreateStudent from "../components/layout/admin/CreateStudent";
import { NavLink } from "react-router-dom";

type TRotue = {
  path: string;
  element: ReactNode;
};
type TItems = {
  key: string;
  label: ReactNode;
  children?: TItems[];
};

const adminPaths = [
  {
    name: "Dashboard",
    path: "dashboard",
    element: <AdminDashboard />,
  },
  {
    name: "User Management",
    children: [
      {
        name: "Create Admin",
        path: "create-admin",
        element: <CreateAdmin />,
      },
      {
        name: "Create Faculty",
        path: "create-faculty",
        element: <CreateFaculty />,
      },
      {
        name: "Create Student",
        path: "create-student",
        element: <CreateStudent />,
      },
    ],
  },
];

export const adminSidebarItems = adminPaths.reduce((acc: TItems[], item) => {
  if (item.path && item.name) {
    acc.push({
      key: item.name,
      label: <NavLink to={`/admin/${item.path}`}>{item.name}</NavLink>,
    });
  }

  if (item.children) {
    acc.push({
      key: item.name,
      label: <NavLink to={`/admin/${item.path}`}>{item.name}</NavLink>,
      children: item.children.map((child) => ({
        key: child.name,
        label: <NavLink to={`/admin/${child.path}`}>{child.name}</NavLink>,
      })),
    });
  }

  return acc;
}, []);

//===========PROGRAMATIC WAY ===================
export const adminRoutes = adminPaths.reduce((acc: TRotue[], item) => {
  if (item.path && item.element) {
    acc.push({
      path: item.path,
      element: item.element,
    });
  }

  if (item.children) {
    item.children.forEach((child) => {
      acc.push({
        path: child.path,
        element: child.element,
      });
    });
  }

  return acc;
}, []);

